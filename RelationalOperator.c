#include"stdio.h"
int main(){
    int a= 5, b= 2;

    // Relational Operators in C.
    printf("Is a == b : %d\n", (a == b));
    printf("Is a != b : %d\n", (a != b));
    printf("Is a > b : %d\n", (a> b));
    printf("Is a < b : %d\n", (a< b));
    printf("Is a >= b : %d\n", (a >= b));
    printf("Is a <= b : %d\n", (a <= b));

    return 0;
}